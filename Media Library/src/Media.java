public class Media {
    String mName;
    String mDesc;

    public Media(String x, String y){
        mName = x;
        mDesc = y;
    }

    public String getmDesc() {
        return mDesc;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public void setmDesc(String mDesc) {
        this.mDesc = mDesc;
    }

}

